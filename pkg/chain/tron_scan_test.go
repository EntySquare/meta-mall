func getMaxBlock(apiKey string) (uint64, error) {
	url := "https://trx.getblock.io/mainnet/fullnode/jsonrpc"

	data := map[string]interface{}{
		"jsonrpc": "2.0",
		"method":  "eth_syncing",
		"params":  []interface{}{},
		"id":      "getblock.io",
	}

	jsonData, err := json.Marshal(data)
	if err != nil {
		return 0, err
	}

	req, err := http.NewRequest("POST", url, bytes.NewBuffer(jsonData))
	if err != nil {
		return 0, err
	}

	req.Header.Set("x-api-key", apiKey)
	req.Header.Set("Content-Type", "application/json")

	client := &http.Client{}
	resp, err := client.Do(req)
	if err != nil {
		return 0, err
	}
	defer resp.Body.Close()

	body, _ := ioutil.ReadAll(resp.Body)

	var result map[string]interface{}
	json.Unmarshal(body, &result)

	syncing := result["result"].(map[string]interface{})
	highestBlock := syncing["highestBlock"].(string)

	highestBlockDec, err := strconv.ParseUint(highestBlock[2:], 16, 64)
	if err != nil {
		return 0, err
	}

	return highestBlockDec, nil
}

