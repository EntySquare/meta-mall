func ethAddressToTronAddress(ethAddress string) (string, error) {
	// 将以太坊地址解码为原始字节表示形式
	ethAddressBytes, err := hex.DecodeString(strings.TrimPrefix(ethAddress, "0x"))
	if err != nil {
		return "", err
	}

	// 用波场地址前缀替换以太坊地址前缀
	tronAddressBytes := append([]byte{0x41}, ethAddressBytes[1:]...)

	// 计算波场地址的校验和
	hash256 := sha256.New()
	hash256.Write(tronAddressBytes)
	firstHash := hash256.Sum(nil)
	hash256.Reset()
	hash256.Write(firstHash)
	secondHash := hash256.Sum(nil)
	checksum := secondHash[:4]

	// 将波场地址字节与校验和合并，生成原始地址
	rawAddress := append(tronAddressBytes, checksum...)

	// 使用 Base58 编码生成波场地址字符串表示形式
	tronAddress := base58.Encode(rawAddress)
	if err != nil {
		panic(err)
	}
	return tronAddress, nil
}
